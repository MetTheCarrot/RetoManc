package carrot.mc.mancchallenge.Utils;

public class ConvertTypesUtils {

    public static boolean bool(String value){
        return Boolean.parseBoolean(value);
    }

    public static String str(int value){
        return String.valueOf(value);
    }

    public static String str(double value){
        return String.valueOf(value);
    }

    public static String str(float value){
        return String.valueOf(value);
    }

    public static String str(long value){
        return String.valueOf(value);
    }

    public static String str(boolean value){
        return String.valueOf(value);
    }

    public static String str(char value){
        return String.valueOf(value);
    }

    public static String str(byte value){
        return String.valueOf(value);
    }

    public static String str(short value){
        return String.valueOf(value);
    }

    public static String str(Object value){
        return String.valueOf(value);
    }

    public static int integer(String value){
        return Integer.parseInt(value);
    }

    public static int integer(double value){
        return (int) value;
    }

    public static int integer(float value){
        return (int) value;
    }

    public static int integer(long value){
        return (int) value;
    }


}
